CREATE TABLE INSTITUCION (	
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
	NOMBRE VARCHAR2(255 BYTE), 
	IDENTIFICADOR VARCHAR2(255 BYTE), 
	ESACTIVO NUMBER(1) DEFAULT (1), 
	USUARIO VARCHAR2(50 BYTE), 
	FECHA DATE,
   CONSTRAINT INSTITUCION_PK PRIMARY KEY 
  (
    ID 
  ));
  
CREATE TABLE TARJETA (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  NUMERO VARCHAR(16 BYTE) NOT NULL,
  INSTITUCION_ID NUMBER NOT NULL,
  ESACTIVO NUMBER(1) DEFAULT(1),
  USUARIO VARCHAR (50 BYTE),
  FECHA DATE,
  CONSTRAINT TARJETA_PK PRIMARY KEY
  (
  ID
  ),
  CONSTRAINT TARJETA_FK FOREIGN KEY 
  (
  INSTITUCION_ID 
  )
  REFERENCES INSTITUCION(ID));